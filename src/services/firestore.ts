export async function seedDefaultContent(seedData: {
  hero: FirestoreHeroContent
  about: FirestoreAbout
  skills: FirestoreSkillItem[]
  projects: FirestoreProjectItem[]
  services: FirestoreServiceItem[]
  theme: FirestoreSiteTheme
}) {
  const batch = writeBatch(db)
  batch.set(doc(db, 'heroContent', 'main'), seedData.hero)
  batch.set(doc(db, 'about', 'main'), seedData.about)
  batch.set(doc(db, 'skills', 'main'), { items: seedData.skills })
  batch.set(doc(db, 'projects', 'main'), { items: seedData.projects })
  batch.set(doc(db, 'services', 'main'), { items: seedData.services })
  batch.set(doc(db, 'siteTheme', 'main'), seedData.theme)
  await batch.commit()
}
