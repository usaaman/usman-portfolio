const toDate = (value: unknown): Date | null => {
  if (!value) return null
  if (value instanceof Date) return value
  if (typeof value === 'object' && value !== null && 'toDate' in value) {
    return (value as { toDate: () => Date }).toDate()
  }
  return null
}

async function ensureFirestoreWriteAuth(): Promise<void> {
  const user = auth.currentUser
  if (!user) {
    throw new Error('You must be logged in to save. Please sign in again.')
  }
  await user.getIdToken(true)
}

export async function fetchHeroContent(): Promise<FirestoreHeroContent | null> {
  const snap = await getDoc(doc(db, 'heroContent', 'main'))
  return snap.exists() ? (snap.data() as FirestoreHeroContent) : null
}

