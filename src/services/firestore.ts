export async function fetchSkills(): Promise<FirestoreSkillItem[]> {
  const snap = await getDoc(doc(db, 'skills', 'main'))
  if (!snap.exists()) return []
  const items = (snap.data().items ?? []) as FirestoreSkillItem[]
  return [...items].sort((a, b) => a.order - b.order)
}

